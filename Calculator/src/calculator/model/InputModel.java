package calculator.model;

public class InputModel {
	
	String firstInputString = "";
	String secondInputString = "";
	String arithmeticSignString = "";
	int arithmeticSignInt = 0;
	
	public String getFirstInputString() {
		return firstInputString;
	}
	public void setFirstInputString(String firstInputString) {
		this.firstInputString = firstInputString;
	}
	public String getSecondInputString() {
		return secondInputString;
	}
	public void setSecondInputString(String secondInputString) {
		this.secondInputString = secondInputString;
	}
	public String getArithmeticSignString() {
		return arithmeticSignString;
	}
	public void setArithmeticSignString(String arithmeticSignString) {
		this.arithmeticSignString = arithmeticSignString;
	}
	public int getArithmeticSignInt() {
		return arithmeticSignInt;
	}
	public void setArithmeticSignInt(int arithmeticSignInt) {
		this.arithmeticSignInt = arithmeticSignInt;
	}
	
}
